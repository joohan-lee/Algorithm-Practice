#실전문제 2. 개미 전사
#한칸 떨어져 있는 수들의 합 중 최대값 구하기

n = int(input())

d = [0] * 100

array = list(map(int,input().split()))

d[0] = array[0]
d[1] = max(d[0], array[1])

for i in range(2, n):
 d[i] = max(d[i-1], d[i-2] + array[i])
 
print(d[n-1]) 


