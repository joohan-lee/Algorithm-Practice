#실전문제 3. 바닥 공사
# 작은 타일을 이용해 바닥을 채우기.
# 1X2, 2X1, 2X2 타일을 이용해 2 X N 바닥을 채우는 모든 경우의 수?

#왼쪽부터 채워간다고 생각하고 i - 1, i - 2번째 경우를 생각해보자
#점화식 : a(i) = a(i-1) + a(i-2) * 2
#최대크기가 2 X 2이므로 위와같은 점화식이 나온다.

n = int(input())

d = [0] * 1001

d[1] = 1
d[2] = 3

for i in range(3, n+1):
  d[i] = (d[i-1] + d[i-2] * 2) % 796796

print(d[n])


