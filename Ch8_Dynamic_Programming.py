#실전문제1. 1로만들기

# 1. x가 5로 나누어떨어지면 5로 나누기.
# 2. x가 3으로 나누어떨어지면 3으로 나누기.
# 3. x가 2로 나누어떨어지면 2로 나누기.
# 4. x에서 1을 빼기.

#x를 위 4개연산으로 1을 만들기.
#최소 연산 횟수는?

x = int(input())

d = [0] * 30001

for i in range(2, x+1):
  #현재의 수에서 1을 빼는 경우
  d[i] = d[i-1] + 1

  #현재의 수가 2로 나누어 떨어지는 경우
  if i % 2 == 0:
    d[i] = min(d[i],d[i//2]+1)
  
  #현재의 수가 3으로 나누어 떨어지는 경우
  if i % 3 == 0:
    d[i] = min(d[i], d[i // 3] + 1)

  if i % 5 == 0:
    d[i] = min(d[i],d[i // 5] + 1)

print(d[x])


